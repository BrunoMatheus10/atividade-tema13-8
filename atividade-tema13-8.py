# Recebe a entrada do usuário
campo = input("Digite algo: ")

# Verifica se o campo está vazio
if campo == "":
    # Se a string estiver vazia, imprime que o campo está vazio
    print("O campo está vazio")
else:
    # Se a string não estiver vazia, imprime que o campo não está vazio
    print("O campo não está vazio")
